package 第五章;

import java.util.Random;

public class Ssq {
	public static void main(String[] args) throws InterruptedException {
		System.out.println("双色球开奖结果:");
		System.out.println("六个红球号码是:");
		Random r = new Random();
		int hq;
		for (int i = 0; i < 6; i++) {
			hq = r.nextInt(33) + 1;
			System.out.print(hq + " ");
			Thread.sleep(1000);
		}
		Thread.sleep(1000);
		System.out.println("\n一个篮球号码是:");
		Thread.sleep(1000);
		int lq = r.nextInt(16) + 1;
		System.out.println(lq + " ");

	}
}
