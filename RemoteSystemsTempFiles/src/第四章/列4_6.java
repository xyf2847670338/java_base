class Animal{
		@Override
		public String toString() {
			return"这是一只来自天堂的普罗米修斯-卢义澳";
		}
	}
class Dog extends Animal{
	@Override
	public String toString() {
		return"(\r\n" + 
				"         ____()()\r\n" + 
				"        /      @@\r\n" + 
				"  `~~~~~\\_;m__m._>o )去吧-卢义澳";
	}
}
public class 列4_6 {
	public static void main(String[] args) {
		Animal animal=new Animal();
		Dog lya=new Dog();
		System.out.println(animal.toString());
		System.out.println(lya.toString());
	}
}
